/**
 * números de 20 en 20 desde 320 hasta 160 con do while
 *
 *@author Pedro Jiménez
 */
 
public class Ejercicio6tema5 {
    public static void main (String[] args) {
        
        int n = 320;
        
        do {
            System.out.println(n);
            n -= 20;
        } while (n > 159);
    }
}
