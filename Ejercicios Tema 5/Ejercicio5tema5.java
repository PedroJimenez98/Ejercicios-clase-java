/**
 * números de 20 en 20 desde 320 hasta 160 con while
 *
 *@author Pedro Jiménez
 */
 
public class Ejercicio5tema5 {
    public static void main (String[] args) {
        
        int n = 320;
        
        while (n > 159) {
            System.out.println(n);
            n -= 20;
        }
    }
}
